function arrayPlusArr01(arr1, arr2) {
  return [...arr1, ...arr2].reduce((acc, cur) => acc + cur, 0);
}
console.log(arrayPlusArr01([1, 2, 3], [4, 5, 6]));

//  ╭─────────────────────────────────────────────────────────────────────────╮
//  │ METHOD 02                                                               │
//  ╰─────────────────────────────────────────────────────────────────────────╯
function arrayPlusArr02(arr1, arr2) {
  return arr1.concat(arr2).reduce((acc, cur) => acc + cur, 0);
}
console.log(arrayPlusArr02([1, 2, 3], [4, 5, 6]));
