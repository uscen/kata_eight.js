function maps01(x) {
  return x.map((n) => n * 2);
}
console.log(maps01([1, 2, 3]));
console.log(maps01([2, 4, 6]));

//  ╭─────────────────────────────────────────────────────────────────────────╮
//  │ METHOD 02                                                               │
//  ╰─────────────────────────────────────────────────────────────────────────╯
function maps02(x) {
  let result = [];
  x.forEach((n) => result.push(n * 2));
  return result;
}
console.log(maps02([1, 2, 3]));
console.log(maps02([2, 4, 6]));
