function sumMix01(x) {
  return x.map((x) => +x).reduce((acc, cur) => acc + cur);
}
console.log(sumMix01([1, 2, 3, "4", "5", "6"]));

//  ╭─────────────────────────────────────────────────────────────────────────╮
//  │ METHOD 02                                                               │
//  ╰─────────────────────────────────────────────────────────────────────────╯
function sumMix02(x) {
  let result = 0;
  for (let i = 0; i < x.length; i++) {
    result += Number(x[i]);
  }
  return result;
}
console.log(sumMix02([1, 2, 3, "4", "5", "6"]));
