function grow01(x) {
  return x.reduce((acc, curr) => acc * curr, 1);
}
console.log(grow01([1, 2, 3, 4]));

//  ╭─────────────────────────────────────────────────────────────────────────╮
//  │ METHOD 02                                                               │
//  ╰─────────────────────────────────────────────────────────────────────────╯
function grow02(x) {
  let result = 1;
  for (num of x) {
    result *= num;
  }
  return result;
}
console.log(grow02([1, 2, 3, 4]));
